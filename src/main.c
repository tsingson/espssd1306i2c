/**
 * @brief 在指定行列显示字符串
 * @param x 列坐标 (0-127)
 * @param page 页坐标 (0-7)
 * @param str 字符串
 */
void display_string(uint8_t x, uint8_t page, const char *str) {
  uint8_t cursor_x = x;
  while (*str) {
    // 将字符转为数组下标 (假设 ' ' 是 0，'!' 是 1...)
    uint8_t char_idx = *str - 32;

    // 发送 5 字节数据
    ssd1306_send_cmd(0xB0 + page);
    ssd1306_send_cmd(0x00 + (cursor_x & 0x0F));
    ssd1306_send_cmd(0x10 + (cursor_x >> 4));
    ssd1306_send_data((uint8_t *)font5x7[char_idx], 5);

    // 每个字符宽 5 像素 + 1 像素间距
    cursor_x += 6;
    if (cursor_x > 122)
      break; // 防止越界
    str++;
  }
}

void app_main() {
  // 1. 初始化 I2C
  i2c_config_t conf = {
      .mode = I2C_MODE_MASTER,
      .sda_io_num = I2C_MASTER_SDA_IO,
      .scl_io_num = I2C_MASTER_SCL_IO,
      .sda_pullup_en = GPIO_PULLUP_ENABLE,
      .scl_pullup_en = GPIO_PULLUP_ENABLE,
      .master.clk_speed = 400000,
  };
  i2c_param_config(I2C_NUM_0, &conf);
  i2c_driver_install(I2C_NUM_0, I2C_MODE_MASTER, 0, 0, 0);

  // 2. 初始化序列
  uint8_t init_cmds[] = {0xAE, 0x20, 0x00, 0x81, 0xCF, 0xA1, 0xC8, 0xA8,
                         0x3F, 0xD3, 0x00, 0x40, 0x8D, 0x14, 0xAF};
  for (int i = 0; i < sizeof(init_cmds); i++)
    ssd1306_send_cmd(init_cmds[i]);

  // 3. 清屏 (写入 1024 字节的 0x00)
  uint8_t clear_buf[128] = {0};
  for (int page = 0; page < 8; page++) {
    ssd1306_send_cmd(0xB0 + page); // 设置页
    ssd1306_send_cmd(0x00);        // 列低位
    ssd1306_send_cmd(0x10);        // 列高位
    for (int i = 0; i < 8; i++)
      ssd1306_send_data(clear_buf, 128); // 填 0
  }

  printf("Screen cleared. Ready for your data.\n");

  // 只需要这样一行：
  display_string(0, 0, "Hello World 123");

  while (1)
    vTaskDelay(pdMS_TO_TICKS(1000));
}
