#define I2C_MASTER_SCL_IO 22
#define I2C_MASTER_SDA_IO 21
#define SSD1306_ADDR 0x3C

// 发送命令
void ssd1306_send_cmd(uint8_t cmd) {
  uint8_t buf[2] = {0x00, cmd};
  i2c_master_write_to_device(I2C_NUM_0, SSD1306_ADDR, buf, 2,
                             pdMS_TO_TICKS(100));
}

// 发送数据 (显存)
void ssd1306_send_data(uint8_t *data, size_t len) {
  uint8_t buf[len + 1];
  buf[0] = 0x40; // 0x40 代表数据流模式
  for (size_t i = 0; i < len; i++)
    buf[i + 1] = data[i];
  i2c_master_write_to_device(I2C_NUM_0, SSD1306_ADDR, buf, len + 1,
                             pdMS_TO_TICKS(100));
}

